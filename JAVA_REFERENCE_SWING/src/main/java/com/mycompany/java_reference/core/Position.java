package com.mycompany.java_reference.core;

/**
 *
 * @author kraks
 */
public class Position {
    private int id;
    private String name;
    
    public Position(int id, String name)
    {
        this.id = id;
        this.name = name;
    }
    
    public int getId()
    {
        return this.id;
    }
    
    public void setId(int id)
    {
        this.id = id;
    }
    
    public void setName(String name)
    {
        this.name = name;
    }
    
    public String getName()
    {
        return this.name;
    }
    
}
